#define CRTC_VSYNC_START_MASK	0xFFFF
#define CRTC_VSYNC_END_SHIFT	16
#define CRTC_VSYNC_END_MASK	0x3FFF
#define CRTC_VSYNC_POLSE	BIT(30)
#define CRTC_VSYNC_POL		BIT(31)

#define DC_CRTC_GAMINDEX_REG		(0x14E0)
