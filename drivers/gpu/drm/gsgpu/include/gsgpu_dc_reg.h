#define DC_CRTC_VDISPLAY_REG		(0x1480)
#define CRTC_VPIXEL_SHIFT	0
#define CRTC_VPIXEL_MASK	0xFFFF
#define CRTC_VTOTAL_SHIFT	16
#define CRTC_VTOTAL_MASK	0xFFFF

#define DC_CRTC_VSYNC_REG		(0x14A0)
