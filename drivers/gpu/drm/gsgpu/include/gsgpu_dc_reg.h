#define DC_REG_INCREASE 0x10
#define CURRENT_REG(x, link)  ((x) + (link * DC_REG_INCREASE))

#define HT1LO_PCICFG_BASE 0xefdfe000000UL
#define LS7A_PCH_CFG_SPACE_REG (TO_UNCAC(HT1LO_PCICFG_BASE)|0x0000a810)
