#define CRTC_GAMMA_BLUE_SHIFT	0
#define CRTC_GAMMA_BLUE_MASK	0xFF
#define CRTC_GAMMA_GREEN_SHIFT	8
#define CRTC_GAMMA_GREEN_MASK	0xFF
